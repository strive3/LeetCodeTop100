public class HasCycle141 {

   public boolean hasCycle(ListNode head) {
      HashSet<ListNode> set = new HashSet<>();
      while (head != null) {
         if (set.contains(head)) {
            return true;
         }
         set.add(head);
         head = head.next;
      }
      return false;
   }

   public boolean hasCycle2(ListNode head) {
      ListNode fast = head;
      ListNode slow = head;
      while (fast != null && fast.next != null) {
         fast = fast.next.next;
         slow = slow.next;
         if (fast == slow) {
            return true;
         }
      }
      return false;
   }


}
